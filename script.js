// ========== ОТОБРАЖЕНИЕ ГЛАВНОГО МЕНЮ ==========
function renderMainMenu() {
    if (!DOM.sectionsContainer) {
        console.error('Контейнер sectionsContainer не найден!');
        return;
    }
    
    const fragment = document.createDocumentFragment();
    courseData.sections.forEach(section => {
        const card = document.createElement('div');
        card.className = 'section-card';
        card.dataset.section = section.id;
        card.innerHTML = `
            <div class="section-number">${section.id}</div>
            <div class="section-title">${section.name}</div>
            <div class="section-desc">${section.description}</div>
        `;
        fragment.appendChild(card);
    });
    
    DOM.sectionsContainer.innerHTML = '';
    DOM.sectionsContainer.appendChild(fragment);
}

// ========== ЗАГРУЗКА ПОЛЬЗОВАТЕЛЯ ==========
function loadUser() {
    const saved = localStorage.getItem('digitalNavigatorUser');
    if (saved) {
        currentUser = JSON.parse(saved);
        updateUIForUser();
    }
}

function saveUser() {
    if (currentUser) {
        localStorage.setItem('digitalNavigatorUser', JSON.stringify(currentUser));
    }
}

function updateUIForUser() {
    if (!DOM.userGreeting) return;
    
    if (currentUser) {
        DOM.userGreeting.style.display = 'flex';
        document.getElementById('userName').textContent = currentUser.name;
        document.getElementById('loginBtn').style.display = 'none';
    } else {
        DOM.userGreeting.style.display = 'none';
        document.getElementById('loginBtn').style.display = 'inline-block';
    }
}

