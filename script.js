function showProfile() {
    if (!currentUser) return;
    
    document.getElementById('profilePhone').textContent = currentUser.phone;
    document.getElementById('profileName').textContent = currentUser.name;

    const progressList = document.getElementById('progressList');
    progressList.innerHTML = '';
    const completedLessons = currentUser.progress || [];
    if (completedLessons.length === 0) {
        progressList.innerHTML = '<p>Вы ещё не прошли ни одного урока.</p>';
    } else {
        const allLessons = courseData.sections.flatMap(s => s.lessons);
        completedLessons.forEach(lessonId => {
            const lesson = allLessons.find(l => l.id == lessonId);
            if (lesson) {
                const item = document.createElement('div');
                item.className = 'progress-item';
                item.textContent = `✅ ${lesson.name}`;
                progressList.appendChild(item);
            }
        });
    }

    const certsList = document.getElementById('certificatesList');
    certsList.innerHTML = '';
    const certs = currentUser.certificates || [];
    if (certs.length === 0) {
        certsList.innerHTML = '<p>У вас пока нет сгенерированных справок.</p>';
    } else {
        certs.forEach(cert => {
            const item = document.createElement('div');
            item.className = 'certificate-item';
            item.textContent = `📄 ${cert.name} от ${cert.date} (№${cert.number})`;
            certsList.appendChild(item);
        });
    }

    DOM.mainMenu.style.display = 'none';
    DOM.lessonsList.style.display = 'none';
    DOM.lessonPage.style.display = 'none';
    DOM.profilePage.style.display = 'block';
}

function showMainMenu() {
    DOM.mainMenu.style.display = 'block';
    DOM.lessonsList.style.display = 'none';
    DOM.lessonPage.style.display = 'none';
    DOM.profilePage.style.display = 'none';
}

