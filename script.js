// ========== НАВИГАЦИЯ МЕЖДУ УРОКАМИ ==========
function goToPrevLesson() {
    if (!currentSection || !currentLesson) return;
    
    const lessons = currentSection.lessons;
    const currentIndex = lessons.findIndex(l => l.id === currentLesson.id);
    
    if (currentIndex > 0) {
        const prevLesson = lessons[currentIndex - 1];
        openLesson(currentSection.id, prevLesson.id);
    } else {
        showMessage('Это первый урок в разделе. Предыдущего урока нет.');
    }
}

function goToNextLesson() {
    if (!currentSection || !currentLesson) return;
    
    const lessons = currentSection.lessons;
    const currentIndex = lessons.findIndex(l => l.id === currentLesson.id);
    
    if (currentIndex < lessons.length - 1) {
        const nextLesson = lessons[currentIndex + 1];
        openLesson(currentSection.id, nextLesson.id);
    } else {
        showMessage('Это последний урок в разделе. Следующего урока нет.');
    }
}

// ========== ОТКРЫТИЕ РАЗДЕЛА ==========
function openSection(sectionId) {
    const section = courseData.sections.find(s => s.id == sectionId);
    if (!section) return;
    currentSection = section;
    
    document.getElementById('currentSectionName').textContent = section.name;
    document.getElementById('sectionTitle').textContent = section.name;

    const lessonsContainer = document.getElementById('lessonsContainer');
    lessonsContainer.innerHTML = '';
    
    const fragment = document.createDocumentFragment();
    section.lessons.forEach(lesson => {
        const card = document.createElement('div');
        card.className = 'lesson-card';
        if (currentUser && currentUser.progress && currentUser.progress.includes(lesson.id)) {
            card.classList.add('completed');
        }
        card.dataset.lessonId = lesson.id;
        card.innerHTML = `
            <div class="lesson-card-number">Урок ${lesson.id}</div>
            <div class="lesson-card-title">${lesson.name}</div>
            <div class="lesson-card-desc">${lesson.description}</div>
        `;
        card.addEventListener('click', () => openLesson(section.id, lesson.id));
        fragment.appendChild(card);
    });
    lessonsContainer.appendChild(fragment);

    DOM.mainMenu.style.display = 'none';
    DOM.lessonsList.style.display = 'block';
    DOM.profilePage.style.display = 'none';
}

// ========== ОТКРЫТИЕ УРОКА ==========
function openLesson(sectionId, lessonId) {
    const section = courseData.sections.find(s => s.id == sectionId);
    const lesson = section.lessons.find(l => l.id == lessonId);
    if (!lesson) return;
    currentLesson = lesson;

    document.getElementById('currentSectionNameBread').textContent = section.name;
    document.getElementById('currentLessonNameBread').textContent = lesson.name;
    document.getElementById('lessonTitle').textContent = lesson.name;
    document.getElementById('lessonGoal').textContent = '🎯 Цель: ' + lesson.goal;

    const stepsContainer = document.getElementById('stepsContainer');
    stepsContainer.innerHTML = '';

    if (lesson.steps && lesson.steps.length > 0) {
        lesson.steps.forEach((step, index) => {
            const stepDiv = document.createElement('div');
            stepDiv.className = 'step-item';
            
            stepDiv.innerHTML = `
                <div class="step-caption">
                    <strong>➡️ Шаг ${index + 1}:</strong> ${step.caption}
                </div>
                <div class="step-image">
                    <img src="${step.image}" alt="Шаг ${index + 1}" 
                         style="max-width: 100%; border-radius: 20px; max-height: 350px;"
                         onerror="this.onerror=null; this.parentElement.innerHTML='<span>❌ Изображение не найдено</span>';">
                </div>
            `;
            
            // Добавляем обработчик клика на изображение для увеличения
            const img = stepDiv.querySelector('.step-image img');
            if (img && img.src && !img.src.includes('undefined')) {
                img.addEventListener('click', (e) => {
                    e.stopPropagation();
                    openLightbox(img.src);
                });
            }
            
            stepsContainer.appendChild(stepDiv);
        });
    } else {
        stepsContainer.innerHTML = '<p class="text-center">Для этого урока пока нет подробной инструкции.</p>';
    }

    DOM.lessonsList.style.display = 'none';
    DOM.lessonPage.style.display = 'block';
    
    // Прокрутка страницы вверх после открытия урока
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

