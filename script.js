function handleSendCode() {
    const phone = document.getElementById('phoneInput').value;
    if (phone) {
        document.getElementById('codeSection').style.display = 'block';
        document.getElementById('loginMessage').textContent = 'Код отправлен (введите 1111)';
    }
}

function handleVerifyCode() {
    const code = document.getElementById('codeInput').value;
    if (code === '1111') {
        const phone = document.getElementById('phoneInput').value;
        currentUser = {
            phone: phone,
            name: 'Иван Иванович',
            progress: [],
            certificates: []
        };
        saveUser();
        updateUIForUser();
        hideLoginModal();
        document.getElementById('codeInput').value = '';
    } else {
        document.getElementById('loginMessage').textContent = 'Неверный код';
    }
}

function logout() {
    currentUser = null;
    localStorage.removeItem('digitalNavigatorUser');
    updateUIForUser();
    showMainMenu();
}

